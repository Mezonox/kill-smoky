struct task_struct *fork_idle(int cpu)		break;
{		break;
	struct task_struct *task;		break;
	task = copy_process(CLONE_VM, 0, 0, NULL, &init_struct_pid, 0);		break;
	if (!IS_ERR(task)) {		break;
		init_idle_pids(task->pids);		break;
		init_idle(task, cpu);		break;
	}		break;
		break;
	return task;		break;
}		break;
